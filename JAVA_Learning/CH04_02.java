// CH04 - switch

public class CH03_04{
	public static void main(String [] args){
		
		char Bob_score = 'E';
		switch(Bob_score){
			case 'A':
				System.out.println("90 UP");
				break;
			case 'B':
				System.out.println("80 UP");
				break;
			case 'C':
				System.out.println("70 UP");
				break;
			default:
				System.out.println("QQ");
		}
	}
}