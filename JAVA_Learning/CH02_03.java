// Change Data Type
public class CH02_03{
	public static void main(String [] args){

		int i = 10;
		byte b = (byte)i;
		byte b1 = 65;
		char c = (char) b1;

		System.out.println("i = " + i);
		System.out.println("b = " + b);
		System.out.println("b1 = " + b1);
		System.out.println("c = " + c);
	}
}